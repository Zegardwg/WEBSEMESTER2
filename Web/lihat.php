    <h1>Panduan Pemesanan Bus Trans Madiun</h1>
    <ol>
        <li>
            <span class="step-title">Mendaftar Akun:</span>
            <ul>
                <li>Jika Anda belum memiliki akun, langkah pertama adalah mendaftar di situs resmi Trans Madiun.</li>
                <li>Kunjungi halaman pendaftaran dan isi formulir dengan informasi yang diperlukan seperti nama, alamat email, nomor telepon, dan buat kata sandi.</li>
            </ul>
        </li>
        <li>
            <span class="step-title">Memilih Rute Perjalanan:</span>
            <ul>
                <li>Setelah berhasil masuk ke akun Anda, pilih menu "Pemesanan" atau "Rute".</li>
                <li>Pilih rute perjalanan yang diinginkan dari daftar rute yang tersedia.</li>
                <li>Pastikan untuk memilih rute yang sesuai dengan tujuan perjalanan Anda.</li>
            </ul>
        </li>
        <li>
            <span class="step-title">Memilih Jadwal Keberangkatan:</span>
            <ul>
                <li>Setelah memilih rute, pilih jadwal keberangkatan yang tersedia.</li>
                <li>Anda dapat melihat berbagai pilihan jadwal berdasarkan tanggal dan waktu yang diinginkan.</li>
                <li>Pilih jadwal keberangkatan yang paling sesuai dengan rencana Anda.</li>
            </ul>
        </li>
        <li>
            <span class="step-title">Memilih Shelter Keberangkatan dan Tujuan:</span>
            <ul>
                <li>Tentukan dari shelter mana Anda akan berangkat dan shelter mana tujuan Anda.</li>
                <li>Shelter keberangkatan dan tujuan biasanya tertera pada peta rute di situs.</li>
            </ul>
        </li>
        <li>
            <span class="step-title">Memilih Tempat Duduk:</span>
            <ul>
                <li>Setelah menentukan jadwal dan shelter, pilih tempat duduk Anda di bus.</li>
                <li>Anda dapat melihat peta tempat duduk dan memilih posisi yang diinginkan, apakah dekat jendela, di depan, atau di bagian tengah bus.</li>
            </ul>
        </li>
        <li>
            <span class="step-title">Mencetak Bukti Pemesanan (Barcode):</span>
            <ul>
                <li>Setelah semua pilihan dikonfirmasi, lanjutkan ke pembayaran.</li>
                <li>Setelah pembayaran berhasil, Anda akan mendapatkan bukti pemesanan yang berupa barcode.</li>
                <li>Cetak atau simpan bukti pemesanan ini, karena akan diperlukan saat naik bus.</li>
            </ul>
        </li>
    </ol>
    <p>Pastikan untuk membawa bukti pemesanan dan datang lebih awal di shelter keberangkatan untuk memastikan perjalanan Anda lancar. Semoga panduan ini membantu Anda dalam menggunakan layanan bus Trans Madiun dengan lebih mudah dan nyaman.</p>
